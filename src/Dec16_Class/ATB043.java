package Dec16_Class;

public class ATB043
{
    public static void main(String[] args) {
        //decrement operator
        int b = 10;
        System.out.println(--b + b--);
        System.out.println(b);

        //A --> --b Exp--> 9, b = 9
        //B --> b-- Exp -->9, b--> 8
        //A+B = 18
        //a =8
    }
}
