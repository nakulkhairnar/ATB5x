package Dec10_Class;

public class ATB007
{
    public static void main(String[] args) {
        //showcase difference between print and println
        System.out.print("Nakul");
        System.out.println("Khairnar");
        //println prints the result on the next line
        System.out.println("Nakul");
        System.out.println("Khairnar");
        //Printf for formating
        int age = 27;
        System.out.printf("Your age is %d", age);
        //%d means any integer - byte, short, int, or long,
        //%s -> string
        //%c --> char
        System.out.println("Your age is" +age);

    }
}
